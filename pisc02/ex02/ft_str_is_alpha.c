int	ft_str_is_alpha(char *str)
{
	int	c;

	c = 0;
	while ((str[c] >= 'a' && str[c] <= 'z') || (str[c] >= 'A' && str[c] <= 'Z'))
		c ++;
	if (str[c] == '\0')
		return (1);
	else
		return (0);
}
