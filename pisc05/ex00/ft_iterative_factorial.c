int	ft_iterative_factorial(int nb)
{
	int	c;

	if (nb <= 0)
		return (0);
	else
	{
		c = nb - 1;
		while (c > 0)
		{
			nb = nb * c;
			c--;
		}
		return (nb);
	}
}
