int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	else
	{
		while (i < nb)
		{
			if (nb % i == 0)
				return (0);
			i++;
		}
		return (1);
	}
}

int	ft_find_next_prime(int nb)
{
	while (nb < 46341)
	{
		if (ft_is_prime(nb) == 0)
			nb++;
		else
			return (nb);
	}
	return (0);
}
